import statistics 

def main():
  v = [] # array dove mettere i voti

  # ciclo di lettura dei voti
  while True:
    x = float(input('Inserisci voto (-1 per terminare): '))
    
    if x == -1: # esco da qua
      break
    
    v.append(x) #  aggiungo i voti all'array

    
  print('Minimo = ', min(v))
  #i = index(v)

  # tolgo il pi√π piccolo
  v.remove(min(v))

  print('v =', v, 'statistics.mean(v) =', statistics.mean(v))

main() 
